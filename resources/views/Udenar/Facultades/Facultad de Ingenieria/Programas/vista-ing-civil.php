        <h1 style="color:black;">Sobre el Programa de Ingeniería Civil</h1><br>
        <p>La actual Facultad de Ingeniería se inició en el año de 1971 con el Programa de Ingeniería Civil tomando como base el apoyo ofrecido por el ICFES  a las Universidades que crearan Programas de Transferencia, debido a que la transferencia ofrecía mejores posibilidades de acceso a la población estudiantil alejada de los centros.</p>
        <p>El Consejo de la Universidad de Nariño, acogiendo la recomendación del Consejo Académico aprobó la creación de la Facultad de Ingeniería, mediante Acuerdo No. 035 de Mayo 13 de 1983, siendo el primer Programa el de Ingeniería Civil.</p>
        <p>En la actualidad, el programa de Ingeniería Civil se ofrece en su totalidad en la sede de Pasto y dependiendo de un estudio de viabilidad, ofrece su ciclo básico, hasta cuarto semestre, en otros municipios.</p>
        <p>A partir de su implementación completa en la Universidad de Nariño, el programa de Ingeniería Civil ha adelantado procesos de actualización y mejoramiento continuo de su proyecto educativo, lo cual lo llevó a obtener la acreditación de alta calidad el 21 de febrero de 2011, por un término de cuatro años, mediante la Resolución 1236 del Ministerio de Educación Nacional.</p>
