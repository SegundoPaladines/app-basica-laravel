        <h1 style="color:black;">Sobre el Programa de Ingeniería Electrónica</h1><br>
        <p>El programa de ingeniería electrónica fue creado mediante el Acuerdo 062 del 4 de julio de 2000 emanado del Consejo Superior, y está adscrito a la Facultad de Ingeniería. Posteriormente, recibió el registro calificado mediante la Resolución 1343 del 16 de junio de 2003 del Ministerio de Educación Nacional, renovándose en noviembre de 2010.</p>
        <p>En el año 2013, se crea el Grupo de Investigación en Ingeniería Eléctrica y Electrónica (GIIEE), que ha potenciado la investigación y la proyección social del Programa. El GIIEE contribuyó significativamente a la acreditación de alta calidad del Programa, la cual se concede mediante la Resolución 20128 de 2015 del Ministerio de Educación Nacional y se renueva en el año 2022 por un periodo de 4 años mediante la Resolución 20128 del mismo ministerio.</p>
        <p>Actualmente, el programa mantiene su compromiso de brindar educación de alta calidad a la comunidad del departamento y el país, complementando su labor académica y de investigación con la Maestría en Ingeniería Electrónica de la Universidad de Nariño.</p>
